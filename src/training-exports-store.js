/* Training-exports store — thin CRUD wrapper over the
   `training_exports` table + `training-exports` storage bucket.

   Admin-only via RLS. Consumed by:
     - Export panel: uploadExport() after building the ZIP client-side.
     - Colab notebook: getExportSignedUrl() to stream the ZIP down.
     - Colab notebook: signed upload URL to push the model bundle back
       to the model-artifacts bucket (see modelBundleUploadUrl below). */
import { client } from './supabase-client.js';
import { getLastSession } from './auth.js';

const BUCKET = 'training-exports';
const MODEL_BUCKET = 'model-artifacts';

/* Upload an export manifest (JSON) + insert a metadata row. Path scheme:
     {yyyy-mm-dd}/{uuid}.json
   The manifest lists every photo's path in Supabase Storage plus a
   long-lived signed URL Colab can fetch it from directly. Total size
   ~1MB tops even for tens of thousands of photos — no more giant blob
   uploads, no more Safari-suspended-tab losses. */
export async function uploadExport({ manifest, speciesCount, imageCount, splitSeed, notes }) {
  const c = client();
  if (!c) return { ok: false, error: 'not-configured' };
  if (!manifest) return { ok: false, error: 'missing manifest' };

  const sess = getLastSession();
  const email = sess?.user?.email || null;
  const id = crypto.randomUUID();
  const today = new Date().toISOString().slice(0, 10);
  const storagePath = `${today}/${id}.json`;

  const blob = new Blob([JSON.stringify(manifest, null, 2)], {
    type: 'application/json',
  });

  const up = await c.storage.from(BUCKET).upload(storagePath, blob, {
    contentType: 'application/json',
    upsert: false,
  });
  if (up.error) return { ok: false, error: up.error.message };

  const { error } = await c.from('training_exports').insert({
    id,
    storage_path: storagePath,
    size_bytes:   blob.size,
    species_count: speciesCount,
    image_count:   imageCount,
    split_seed:    splitSeed || null,
    exported_by:   email,
    notes:         notes || null,
  });
  if (error) {
    // Rollback storage so we don't leave orphan bytes.
    await c.storage.from(BUCKET).remove([storagePath]).catch(() => {});
    return { ok: false, error: error.message };
  }
  return { ok: true, id, storagePath };
}

/* Batch-generate signed URLs for a list of storage paths. Wraps the
   plural createSignedUrls so callers can hand in the full photo list
   and get one URL per path back, in the same order. Handles chunking
   because Supabase's endpoint has a hard limit of 1000 paths per
   call. TTL defaults to 24h — plenty for a training run start-to-finish. */
export async function trainingPhotoSignedUrls(paths, ttlSeconds = 86400) {
  const c = client();
  if (!c) return { ok: false, urls: [], error: 'not-configured' };
  const CHUNK = 500;
  const out = new Array(paths.length);
  for (let i = 0; i < paths.length; i += CHUNK) {
    const slice = paths.slice(i, i + CHUNK);
    const { data, error } = await c.storage.from('training-photos')
      .createSignedUrls(slice, ttlSeconds);
    if (error) return { ok: false, urls: [], error: error.message };
    // Supabase returns objects with { path, signedUrl, error } in the
    // same order as the request. Splice into our result slot-by-slot.
    for (let j = 0; j < slice.length; j++) {
      const row = data?.[j];
      out[i + j] = row?.signedUrl || null;
    }
  }
  return { ok: true, urls: out };
}

/* Newest → oldest. */
export async function listExports() {
  const c = client();
  if (!c) return { ok: false, rows: [], error: 'not-configured' };
  const { data, error } = await c.from('training_exports')
    .select('id, storage_path, size_bytes, species_count, image_count, split_seed, exported_by, exported_at, notes')
    .order('exported_at', { ascending: false });
  if (error) return { ok: false, rows: [], error: error.message };
  return { ok: true, rows: data || [] };
}

/* Signed URL for downloading the export ZIP from Colab. Default TTL
   is 6 hours — long enough for a slow first upload + full training
   run. Can be regenerated by re-clicking Copy in the UI. */
export async function getExportSignedUrl(storagePath, ttlSeconds = 6 * 3600) {
  const c = client();
  if (!c) return null;
  const { data, error } = await c.storage.from(BUCKET).createSignedUrl(storagePath, ttlSeconds);
  if (error) return null;
  return data?.signedUrl || null;
}

/* Delete an export (storage + row). */
export async function deleteExport(id, storagePath) {
  const c = client();
  if (!c) return { ok: false, error: 'not-configured' };
  const { error } = await c.from('training_exports').delete().eq('id', id);
  if (error) return { ok: false, error: error.message };
  if (storagePath) await c.storage.from(BUCKET).remove([storagePath]).catch(() => {});
  return { ok: true };
}

/* Signed UPLOAD URL for the model bundle Colab produces at the end of
   training. Colab PUTs the bundle ZIP directly to this URL, no client
   library needed. NOTE: unlike download URLs, createSignedUploadUrl
   does not take a TTL — Supabase fixes the token lifetime at ~2 hours.
   A long training run can outlive it; Colab then gets a 403 and a
   fresh URL must be minted from the Models/Export UI. Path scheme:
     pending/{yyyy-mm-dd}/{uuid}.zip
   The "pending/" prefix flags the bundle as not-yet-imported so the
   Models tab can list it and offer one-click import. */
export async function modelBundleUploadUrl() {
  const c = client();
  if (!c) return { ok: false, error: 'not-configured' };
  const today = new Date().toISOString().slice(0, 10);
  const id = crypto.randomUUID();
  const path = `pending/${today}/${id}.zip`;
  const { data, error } = await c.storage.from(MODEL_BUCKET)
    .createSignedUploadUrl(path);
  if (error) return { ok: false, error: error.message };
  return {
    ok: true,
    signedUrl: data.signedUrl,
    token:     data.token,
    path,
  };
}

/* List pending bundles (already uploaded by Colab, awaiting import).
   Returns rows sorted newest-first. */
export async function listPendingBundles() {
  const c = client();
  if (!c) return { ok: false, rows: [], error: 'not-configured' };
  // list() is bucket-relative; we're listing everything under pending/.
  const { data, error } = await c.storage.from(MODEL_BUCKET).list('pending', {
    limit: 100,
    sortBy: { column: 'created_at', order: 'desc' },
  });
  if (error) return { ok: false, rows: [], error: error.message };
  // list() returns folder entries too — filter to files. Also flatten
  // dated subdirs so the UI sees one flat list.
  const files = [];
  for (const entry of data || []) {
    if (entry.id && entry.name?.endsWith('.zip')) {
      files.push({ path: `pending/${entry.name}`, ...entry });
    } else if (!entry.id) {
      // Directory — recurse one level (that's all we ever have).
      const sub = await c.storage.from(MODEL_BUCKET).list(`pending/${entry.name}`, {
        limit: 100,
        sortBy: { column: 'created_at', order: 'desc' },
      });
      for (const sf of sub.data || []) {
        if (sf.name?.endsWith('.zip')) {
          files.push({ path: `pending/${entry.name}/${sf.name}`, ...sf });
        }
      }
    }
  }
  files.sort((a, b) => (a.created_at < b.created_at ? 1 : -1));
  return { ok: true, rows: files };
}

/* Download a pending bundle to a Blob so the existing importModelVersion
   flow can consume it. Same result as if the user had dropped the ZIP
   on the drop zone. */
export async function downloadPendingBundle(storagePath) {
  const c = client();
  if (!c) return { ok: false, error: 'not-configured' };
  const { data, error } = await c.storage.from(MODEL_BUCKET).download(storagePath);
  if (error) return { ok: false, error: error.message };
  return { ok: true, blob: data };
}

/* Move an imported bundle out of pending/ so it doesn't show up again.
   Non-destructive — copies to imported/ then deletes pending/ copy. */
export async function markBundleImported(pendingPath) {
  const c = client();
  if (!c) return { ok: false, error: 'not-configured' };
  const newPath = pendingPath.replace(/^pending\//, 'imported/');
  const { error: mvErr } = await c.storage.from(MODEL_BUCKET)
    .move(pendingPath, newPath);
  if (mvErr) return { ok: false, error: mvErr.message };
  return { ok: true, newPath };
}
